*+AX_ECLIPTIC	Generate Celestial to ECLIPTIC matrices
	SUBROUTINE AX_ECLIPTIC(DMJD,CTOE,ETOC)
	DOUBLE PRECISION DMJD,CTOE(3,3),ETOC(3,3)
*DMJD	input	MJD epoch
*CTOE	output	Celestial to Ecliptic
*ETOC	output	Ecliptic to Celestial
*-Author Dick Willingale 1991-Feb-22
	DOUBLE PRECISION OBLIQECL,DTOR/0.0174532925199/,DCEL(2),T
C Obliquity of Ecliptic from SLA_ECMAT
	T=(DMJD-51544.5D0)/36525D0
	OBLIQECL=(84381.448D0+(-46.8150D0+(-0.00059D0+0.001813D0*T)*T)*T)
	OBLIQECL=DTOR*OBLIQECL/3600.D0
	DCEL(1)=0.D0
	DCEL(2)=DCEL(1)
	CALL AX_DMAT(DCEL,OBLIQECL,CTOE,ETOC)
	END
